section .text
	global run_method

run_method:
	push rbp
	mov rbp, rsp

	;; store bytecode pointer
	mov r10, rdx

	;; reserve space for local variables (4 bytes per slot)
	mov rax, 8h
	mul rsi
	sub rsp, rax
	;; and save a pointer to the first local variable
	mov r11, rsp

.loop:	xor rax, rax
	mov al, [r10]
	add r10, 1h
	jmp [rax*8+opcodes]

.end:	mov rsp, rbp
	pop rbp
	ret

opcode_impl:
	
.bipush:
	mov al, [r10]
	push rax
	add r10, 1h
	jmp run_method.loop

.iload_0:
	mov eax, [r11]
	push rax
	jmp run_method.loop
.iload_1:
	mov eax, [r11+8h]
	push rax
	jmp run_method.loop
.iload_2:
	mov eax, [r11+10h]
	push rax
	jmp run_method.loop
.iload_3:
	mov eax, [r11+18h]
	push rax
	jmp run_method.loop

.istore_0:
	pop rax
	mov [r11], eax
	jmp run_method.loop
.istore_1:
	pop rax
	mov [r11+8h], eax
	jmp run_method.loop
.istore_2:
	pop rax
	mov [r11+10h], eax
	jmp run_method.loop
.istore_3:
	pop rax
	mov [r11+18h], eax
	jmp run_method.loop

.aload_1:
	mov eax, [r11]
	push rax
	jmp run_method.loop

.iadd:
	pop rax
	pop rbx
	add eax, ebx
	push rax
	jmp run_method.loop

.return:
	xor rax,rax
	jmp run_method.end
.ireturn:
	pop rax
	jmp run_method.end
	